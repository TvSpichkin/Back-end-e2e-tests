import экспресс, {Response, Router} from "express";
import {типЧастицы, типБД} from "../../бд/бд";
import {МодВидаЧаст} from "./модели/МодВидаЧаст";
import {МодЗапрЧаст} from "./модели/МодЗапрЧаст";
import {МодСоздЧаст} from "./модели/МодСоздЧаст";
import {МодОбновЧаст} from "./модели/МодОбновЧаст";
import {МодУИРПарамовИдЧаст} from "./модели/МодУИРПарамовИдЧаст";
import {ЗапросСПарами, ЗапросСТелом, ЗапросСВопросом, ЗапросСПарамиИТелом} from "../../типы";


function провОшибокВводаЧаст(запр: ЗапросСТелом<МодСоздЧаст>, отв: Response): boolean {
    if(!запр.body.название || typeof запр.body.название !== "string" || !запр.body.название.replace(/ /g, "")) {
        отв.sendStatus(400);
        return true;
    }
    return false;
}

function бдВвид(бдЧаст: типЧастицы): МодВидаЧаст {
    return {
        ид: бдЧаст.ид,
        название: бдЧаст.название
    };
}

export function полМаршЧастиц(бд: типБД): Router {
    const марш = экспресс.Router();
    
    марш.get(encodeURI("/"), (запр: ЗапросСВопросом<МодЗапрЧаст>, отв: Response<МодВидаЧаст[]>) => {
        var найдЧаст = бд.частицы;
        //console.log("запр.query.название = " + запр.query.название);
        if(запр.query.название) найдЧаст = найдЧаст.filter(ч => ч.название.indexOf(запр.query.название) > -1);

        отв.json(найдЧаст.map(бдВвид));
    });
    марш.get(encodeURI("/:id"), (запр: ЗапросСПарами<МодУИРПарамовИдЧаст>, отв: Response<МодВидаЧаст>) => {
        const найдЧаст = бд.частицы.find(ч => ч.ид === +запр.params.id);

        if(!найдЧаст) {
            отв.sendStatus(404);
            return 404;
        }

        отв.json(бдВвид(найдЧаст));
    });
    марш.post(encodeURI("/"), (запр: ЗапросСТелом<МодСоздЧаст>, отв: Response<МодВидаЧаст>) => {
        if(провОшибокВводаЧаст(запр, отв)) return 400;

        const добЧаст = {
            ид: бд.частицы.length ? бд.частицы[бд.частицы.length - 1].ид + 1 : 1,
            название: запр.body.название,
            количество: 0
        };

        бд.частицы.push(добЧаст);

        отв.status(201).json(бдВвид(добЧаст));
    });
    марш.delete(encodeURI("/:id"), (запр: ЗапросСПарами<МодУИРПарамовИдЧаст>, отв: Response) => {
        const найдЧаст = бд.частицы.find(ч => ч.ид === +запр.params.id);

        if(!найдЧаст) {
            отв.sendStatus(404);
            return 404;
        }

        бд.частицы = бд.частицы.filter(ч => ч.ид !== +запр.params.id);

        отв.sendStatus(204);
    });
    марш.put(encodeURI("/:id"), (запр: ЗапросСПарамиИТелом<МодУИРПарамовИдЧаст, МодОбновЧаст>, отв: Response) => {
        if(провОшибокВводаЧаст(запр, отв)) return 400;

        const найдЧаст = бд.частицы.find(ч => ч.ид === +запр.params.id);

        if(!найдЧаст) {
            отв.sendStatus(404);
            return 404;
        }

        найдЧаст.название = запр.body.название;

        отв.sendStatus(204);
    });

    return марш;
}
